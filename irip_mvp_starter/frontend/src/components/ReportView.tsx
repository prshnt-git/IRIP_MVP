// ============================================================
// Props
// ============================================================

interface ReportViewProps {
  productId: string;
  competitorId: string;
  startDate: string;
  endDate: string;
  productName: string;
  dashboard: VisualDashboard | null;
}

type ReportStatus = "idle" | "loading" | "loaded" | "error";

// ============================================================
// Pure helpers
// ============================================================

function formatPeriod(start: string, end: string): string {
  if (!start && !end) return "All periods";
  if (start && end) return `${start} – ${end}`;
  if (start) return `From ${start}`;
  return `Until ${end}`;
}

function todayLabel(): string {
  return new Date().toLocaleDateString("en-IN", {
    day: "2-digit",
    month: "short",
    year: "numeric",
  });
}

// ============================================================
// PDF generation
// ============================================================

function downloadPdf(
  report: ExecutiveReport,
  productName: string,
  period: string
): void {
  const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
  const PAGE_W = 210;
  const MARGIN = 20;
  const TEXT_W = PAGE_W - MARGIN * 2;
  const dateLabel = todayLabel();

  let y = 22;

  function maybeNewPage(needed: number): void {
    if (y + needed > 274) {
      writeFooter();
      doc.addPage();
      y = 22;
    }
  }

  function writeFooter(): void {
    const page = doc.getNumberOfPages();
    doc.setFontSize(8);
    doc.setFont("helvetica", "normal");
    doc.setTextColor(150, 150, 150);
    doc.text(
      `Generated by IRIP  |  Confidential  |  ${dateLabel}  |  Page ${page}`,
      MARGIN,
      288
    );
    doc.setTextColor(0, 0, 0);
  }

  function heading(text: string, size = 13, r = 24, g = 24, b = 27): void {
    maybeNewPage(size + 6);
    doc.setFontSize(size);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(r, g, b);
    doc.text(text, MARGIN, y);
    doc.setTextColor(0, 0, 0);
    y += size * 0.55 + 4;
  }

  function paragraph(text: string, size = 10): void {
    const lines = doc.splitTextToSize(text, TEXT_W) as string[];
    for (const line of lines) {
      maybeNewPage(size * 0.5 + 2);
      doc.setFontSize(size);
      doc.setFont("helvetica", "normal");
      doc.text(line, MARGIN, y);
      y += size * 0.5 + 2;
    }
    y += 2;
  }

  function bullets(items: string[], size = 10): void {
    for (const item of items) {
      const lines = doc.splitTextToSize(`• ${item}`, TEXT_W - 3) as string[];
      for (let i = 0; i < lines.length; i++) {
        maybeNewPage(size * 0.5 + 2);
        doc.setFontSize(size);
        doc.setFont("helvetica", "normal");
        doc.text(i === 0 ? lines[i] : `  ${lines[i]}`, MARGIN, y);
        y += size * 0.5 + 2;
      }
    }
    y += 2;
  }

  // ── Cover header ──────────────────────────────────────────
  doc.setFontSize(20);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(24, 24, 27);
  doc.text("IRIP — Review Intelligence Report", MARGIN, y);
  y += 9;

  doc.setFontSize(13);
  doc.setFont("helvetica", "normal");
  doc.text(productName, MARGIN, y);
  y += 6;

  doc.setFontSize(9);
  doc.setTextColor(113, 113, 122);
  doc.text(
    `Period: ${period}   |   Generated: ${dateLabel}   |   CONFIDENTIAL`,
    MARGIN,
    y
  );
  y += 8;
  doc.setTextColor(0, 0, 0);

  doc.setDrawColor(228, 228, 231);
  doc.setLineWidth(0.4);
  doc.line(MARGIN, y, PAGE_W - MARGIN, y);
  y += 8;

  // ── Executive narrative ────────────────────────────────────
  if (report.executive_narrative) {
    heading("Executive Summary", 14);
    paragraph(report.executive_narrative, 10);
  }

  // ── Confidence ─────────────────────────────────────────────
  heading("Data Confidence", 12);
  paragraph(report.confidence_note, 10);

  // ── Strengths ──────────────────────────────────────────────
  if (report.key_strengths.length > 0) {
    heading("Key Strengths", 12, 22, 101, 52);
    bullets(report.key_strengths.slice(0, 4), 10);
  }

  // ── Risks ──────────────────────────────────────────────────
  if (report.key_risks.length > 0) {
    heading("Key Risks", 12, 185, 28, 28);
    bullets(report.key_risks.slice(0, 4), 10);
  }

  // ── Competitive position ───────────────────────────────────
  if (report.competitor_takeaways.length > 0) {
    heading("Competitive Position", 12);
    bullets(report.competitor_takeaways.slice(0, 3), 10);
  }

  // ── Recommended actions ────────────────────────────────────
  if (report.recommended_actions.length > 0) {
    heading("Recommended Actions", 12);
    bullets(report.recommended_actions.slice(0, 5), 10);
  }

  writeFooter();
  doc.save(
    `irip-report-${report.product_id}-${dateLabel.replace(/ /g, "-")}.pdf`
  );
}

// ============================================================
// Plain text copy
// ============================================================

function buildCopyText(
  report: ExecutiveReport,
  productName: string,
  period: string
): string {
  const sep = "─".repeat(60);
  const lines: string[] = [
    "IRIP — Review Intelligence Report",
    `Product: ${productName}`,
    `Period: ${period}`,
    "CONFIDENTIAL",
    "",
  ];

  if (report.executive_narrative) {
    lines.push("EXECUTIVE SUMMARY", sep, report.executive_narrative, "");
  }

  lines.push("KEY STRENGTHS", sep);
  for (const s of report.key_strengths.slice(0, 4)) lines.push(`• ${s}`);
  lines.push("");

  lines.push("KEY RISKS", sep);
  for (const s of report.key_risks.slice(0, 4)) lines.push(`• ${s}`);
  lines.push("");

  if (report.competitor_takeaways.length > 0) {
    lines.push("COMPETITIVE POSITION", sep);
    for (const s of report.competitor_takeaways.slice(0, 3))
      lines.push(`• ${s}`);
    lines.push("");
  }

  lines.push("RECOMMENDED ACTIONS", sep);
  for (const s of report.recommended_actions.slice(0, 5)) lines.push(`• ${s}`);
  lines.push("", `Generated by IRIP | ${new Date().toISOString()}`);

  return lines.join("\n");
}

